package day06_Increment_decrement;

public class C01_preincrement_postincrement
{
    public static void main(String[] args) {

        int a = 10;

        int b = 20;

        a++;

        System.out.println("13. satirda a : " + a);

        ++a;

        System.out.println("17. satirda a : " + a);

        b--;
        System.out.println("20. satirda b : " + b);

        --b;
        System.out.println("23. satirda b : " + b);


    }
}
